var mark=parseInt(prompt("enter mark :"))
if(mark<=40)
{
	console.write("F");
}
else if(mark>40&&mark<=45)
{
	console.write("E");
}
else if(mark>45&&mark<=55)
{
	console.write("D");
}
else if(mark>55&&mark<=70)
{
	console.write("C");
}
else if(mark>70&&mark<=80)
{
	console.write("B");
}
else if(mark>80&&mark<=90)
{
	console.write("A");
}
else (mark>90)
{
	console.write("O")
}



